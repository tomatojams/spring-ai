public class _04_Operator4 {

  public static void main(String[] args) {
    boolean 김치찌게 = true;
    boolean 된장찌게 = true;
    boolean 제육볶음 = true;

    System.out.println(김치찌게 && 된장찌게 && 제육볶음);
    System.out.println(김치찌게 || 된장찌게 || 제육볶음);
    System.out.println("김치찌게 맛있엉");
  }

}
